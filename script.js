// Parallax effect for hero section
window.addEventListener('scroll', () => {
    const scrolled = window.pageYOffset;
    const heroElements = document.querySelectorAll('.floating-elements .element');
    
    heroElements.forEach((element, index) => {
        const speed = 0.5 + (index * 0.1);
        element.style.transform = `translateY(${scrolled * speed}px)`;
    });
});

// Counter animation for stats
function animateCounter(element, target, duration = 2000) {
    const start = 0;
    const increment = target / (duration / 16);
    let current = start;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= target) {
            current = target;
            clearInterval(timer);
        }
        
        if (target === 100) {
            element.textContent = Math.floor(current) + '%';
        } else if (target > 10) {
            element.textContent = Math.floor(current) + '+';
        } else {
            element.textContent = Math.floor(current);
        }
    }, 16);
}

// Trigger counter animations when stats section is visible
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const stats = entry.target.querySelectorAll('.stat h3');
            stats.forEach(stat => {
                const text = stat.textContent;
                const target = text.includes('%') ? 100 : text.includes('+') ? 50 : 24;
                animateCounter(stat, target);
            });
            statsObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

document.addEventListener('DOMContentLoaded', () => {
    const statsSection = document.querySelector('.about-stats');
    if (statsSection) {
        statsObserver.observe(statsSection);
    }
});

// Smooth reveal animations with bounce effect for project cards
const revealElements = document.querySelectorAll('.section-header, .service-card, .project-card, .contact-item');
const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('revealed');
            // Add bounce animation specifically for project cards
            if (entry.target.classList.contains('project-card')) {
                entry.target.classList.add('bounce-in');
            }
        }
    });
}, { threshold: 0.2 });

document.addEventListener('DOMContentLoaded', () => {
    // Add CSS for reveal animation
    const style = document.createElement('style');
    style.textContent = `
        .section-header,
        .service-card,
        .project-card,
        .contact-item {
            opacity: 0;
            transform: translateY(30px);
            transition: opacity 0.6s ease, transform 0.6s ease;
        }
        
        .section-header.revealed,
        .service-card.revealed,
        .project-card.revealed,
        .contact-item.revealed {
            opacity: 1;
            transform: translateY(0);
        }
        
        .service-card.revealed,
        .project-card.revealed {
            transition-delay: calc(var(--card-index, 0) * 0.1s);
        }
        
        .project-card.bounce-in {
            animation: bounceIn 0.8s cubic-bezier(0.68, -0.55, 0.265, 1.55);
        }
        
        @keyframes bounceIn {
            0% {
                opacity: 0;
                transform: translateY(30px) scale(0.9);
            }
            50% {
                transform: translateY(-10px) scale(1.02);
            }
            100% {
                opacity: 1;
                transform: translateY(0) scale(1);
            }
        }
    `;
    document.head.appendChild(style);
    
    // Add card index for staggered animation
    document.querySelectorAll('.service-card, .project-card').forEach((card, index) => {
        card.style.setProperty('--card-index', index);
    });
    
    revealElements.forEach(el => revealObserver.observe(el));
});
